#ifndef POWER_VALUE_HPP
#define POWER_VALUE_HPP

#include <kml/input_value.hpp>
#include <boost/mpl/math/is_even.hpp>

namespace kml {

template<typename T, unsigned int N>
struct power_value: mpl::eval_if<
   mpl::is_even< mpl::int_<N> >,
   input_value<T>,
   mpl::identity<T> > {};

}

#endif

