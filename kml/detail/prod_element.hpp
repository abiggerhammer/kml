#ifndef PROD_ELEMENT_HPP
#define PROD_ELEMENT_HPP

#include <kml/detail/functor_element.hpp>

namespace kml { namespace detail {

template<typename T>
T prod_element( T const &x, T const &y ) {
	return functor_element<std::multiplies>( x, y );
}

}} // namespace kml::detail

#endif



